// 获取字库（去重后的版本）
function getUniqueChars() {
    const seen = new Set();
    const result = [];
    for (const [simp, trad] of COMMON_CHARS_WITH_TRADITIONAL) {
        if (!seen.has(simp)) {
            seen.add(simp);
            result.push({ simplified: simp, traditional: trad });
        }
    }
    return result;
}

// 根据难度等级返回字库
function getCharsByLevel(level) {
    const uniqueChars = getUniqueChars();
    
    if (level === 1) {
        // 一级字：返回所有精选常用字
        return uniqueChars.slice(0, Math.min(3500, uniqueChars.length));
    } else if (level === 2) {
        // 二级字：所有精选字
        return uniqueChars;
    } else {
        // 三级字：所有精选字 + 补充一些 Unicode 汉字
        const extra = [];
        const basicChars = uniqueChars.slice();
        for (let code = 0x4E00; code <= 0x9FFF && extra.length < 5000; code++) {
            const ch = String.fromCharCode(code);
            if (!SIMPLIFIED_TO_TRADITIONAL[ch]) {
                extra.push({ simplified: ch, traditional: ch });
            }
        }
        return basicChars.concat(extra);
    }
}

// 尝试获取繁体字形，如果失败返回原字
function tryTraditional(char) {
    return SIMPLIFIED_TO_TRADITIONAL[char] || char;
}

// 获取用于行草字体显示的字（优先用繁体）
function getCaoshuChar(char) {
    return SIMPLIFIED_TO_TRADITIONAL[char] || char;
}

// 获取用于普通字体显示的字（简体）
function getRegularChar(char) {
    return TRADITIONAL_TO_SIMPLIFIED[char] || char;
}
