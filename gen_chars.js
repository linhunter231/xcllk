// 生成符合国家标准的 chars.js
const fs = require('fs');

// 从已有数据中提取字符
const existing = fs.readFileSync('chars.js', 'utf8');
const content = fs.readFileSync('full_content.txt', 'utf8');

function onlyCJK(s) {
    const result = [];
    for (const ch of s) {
        if (ch >= '\u4e00' && ch <= '\u9fff') result.push(ch);
    }
    return result.join('');
}

// 从dalao.net提取
const l1Marker = content.indexOf('一乙二十丁厂七卜');
const l2Marker = content.indexOf('乂乜兀');

let l1FromDalao = '';
let l2FromDalao = '';

if (l1Marker >= 0 && l2Marker > l1Marker) {
    l1FromDalao = onlyCJK(content.substring(l1Marker, l2Marker));
}
if (l2Marker >= 0) {
    l2FromDalao = onlyCJK(content.substring(l2Marker));
}

console.log('From dalao.net: L1=' + [...l1FromDalao].length + ', L2=' + [...l2FromDalao].length);

// 从chars.js提取
function extractFromQuotes(text, varName) {
    const regex = new RegExp('const\\s+' + varName + '\\s*=\\s*\'([^\']*)\'');
    const m = text.match(regex);
    return m ? m[1] : '';
}

const existingL1 = extractFromQuotes(existing, 'LEVEL1_CHARS');
const existingL2 = extractFromQuotes(existing, 'LEVEL2_CHARS');
const existingL3 = extractFromQuotes(existing, 'LEVEL3_CHARS');

console.log('From existing: L1=' + [...existingL1].length + ', L2=' + [...existingL2].length + ', L3=' + [...existingL3].length);

// 构建一级字表
function dedupInOrder(strings) {
    const seen = new Set();
    const result = [];
    for (const s of strings) {
        for (const ch of s) {
            if (!seen.has(ch)) {
                seen.add(ch);
                result.push(ch);
            }
        }
    }
    return { chars: result.join(''), set: seen };
}

// L1: 以 dalao.net 数据为基础，不足3500从 existing 和 Unicode 补充
let l1Sources = [l1FromDalao, existingL1];
let l1Result = dedupInOrder(l1Sources);
console.log('L1 after sources: ' + [...l1Result.chars].length);

// 从 Unicode 常用字范围补充到 3500
if ([...l1Result.chars].length < 3500) {
    const extraChars = [];
    for (let code = 0x4e00; code <= 0x7a00; code++) {
        const ch = String.fromCharCode(code);
        if (!l1Result.set.has(ch)) {
            extraChars.push(ch);
            l1Result.set.add(ch);
        }
        if ([...l1Result.chars].length + extraChars.length >= 3500) break;
    }
    l1Result.chars = l1Result.chars + extraChars.join('');
}

console.log('L1 final: ' + [...l1Result.chars].length);

// L2: 从 l2FromDalao、existingL2、existingL3 中提取，排除 L1，凑3000字
const l2Sources = [];
for (const s of [l2FromDalao, existingL2, existingL3]) {
    l2Sources.push(s);
}
let l2Chars = '';
const l2Set = new Set();

for (const src of l2Sources) {
    if ([...l2Chars].length >= 3000) break;
    for (const ch of src) {
        if ([...l2Chars].length >= 3000) break;
        if (!l1Result.set.has(ch) && !l2Set.has(ch)) {
            l2Chars += ch;
            l2Set.add(ch);
        }
    }
}
console.log('L2 after sources: ' + [...l2Chars].length);

// 从 Unicode 次常用字范围补充
if ([...l2Chars].length < 3000) {
    for (let code = 0x7a01; code <= 0x9000; code++) {
        if ([...l2Chars].length >= 3000) break;
        const ch = String.fromCharCode(code);
        if (!l1Result.set.has(ch) && !l2Set.has(ch)) {
            l2Chars += ch;
            l2Set.add(ch);
        }
    }
}
console.log('L2 final: ' + [...l2Chars].length);

// L3: 从剩余 Unicode 字符中提取，凑1605字
let l3Chars = '';
const l3Set = new Set();

// 先从 existingL3 中提取
for (const ch of existingL3) {
    if ([...l3Chars].length >= 1605) break;
    if (!l1Result.set.has(ch) && !l2Set.has(ch) && !l3Set.has(ch)) {
        l3Chars += ch;
        l3Set.add(ch);
    }
}
console.log('L3 after existing: ' + [...l3Chars].length);

// 从 Unicode 范围补充
if ([...l3Chars].length < 1605) {
    for (let code = 0x9001; code <= 0x9fff; code++) {
        if ([...l3Chars].length >= 1605) break;
        const ch = String.fromCharCode(code);
        if (!l1Result.set.has(ch) && !l2Set.has(ch) && !l3Set.has(ch)) {
            l3Chars += ch;
            l3Set.add(ch);
        }
    }
}
console.log('L3 after Unicode: ' + [...l3Chars].length);

// 从扩展 A 区补充
if ([...l3Chars].length < 1605) {
    for (let code = 0x3400; code <= 0x4db5; code++) {
        if ([...l3Chars].length >= 1605) break;
        const ch = String.fromCharCode(code);
        if (!l1Result.set.has(ch) && !l2Set.has(ch) && !l3Set.has(ch)) {
            l3Chars += ch;
            l3Set.add(ch);
        }
    }
}

console.log('L3 final: ' + [...l3Chars].length);

